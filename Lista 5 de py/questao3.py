#Faça um programa, com uma função que necessite de três argumentos, e que forneça a soma desses três argumentos.

#definindo a função e passando três elementos como parametro
def soma (x, y, z):
    result = x + y + z  #fazendo a soma dos parametros 
    return result       #retornando o resultado da soma

#testando a função
print(soma(2,3,4))