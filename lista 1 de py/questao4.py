#Faça um programa que peça o raio de um círculo, calcule e mostre a sua área

#Solicitando ao usuario o valor do raio para calcular a area e definindo o input como float pois precisamos trabalhar com casas decimais
raio = float(input("digite o valor do raio para calular a area de um circulo "))

area = 3.14*raio*raio #salvando o resultado da conta da area na variavel area

print ("a area do circulo eh ", area) #mostrando o resultado na tela
